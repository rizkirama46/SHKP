    $(window).scroll(function () {
        var scroll = $(window).scrollTop();

        if (scroll >= $('#program-submenu').offset().top) {
            $(".program-submenu").addClass("fixed-program-submenu");
            // $(".stickyBtn-sq").css('bottom', "0");
        } else if(scroll <= $('#program-submenu').offset().top){
            $(".program-submenu").removeClass("fixed-program-submenu");
            // $(".stickyBtn-sq").css('bottom', "-70px");
        }
    });

    var prevScrollpos = window.pageYOffset;
    window.onscroll = function() {
    var currentScrollPos = window.pageYOffset;
    if (prevScrollpos > currentScrollPos) {
        // document.getElementById("stickyBtn").style.bottom = "0";
    } else {
        // document.getElementById("stickyBtn").style.top = "-50px";
    }
        prevScrollpos = currentScrollPos;
    }

    $('.btn--embed').click(function(){
        $('.popup-modal').addClass('active-modal');
    })

    var clicked = false;
    const months = ["Januari", "Febuari", "Maret","April", "Mai", "Juni", "Juli", "Agustus", "September", "Oktober", "November", "Desember"];

    var ua = navigator.userAgent.toLowerCase(); 

    $('.tabBacaUpdate').click(function(){
        var url = window.location.href;
        var slug = url.split('/');
        var programHistory = $('.program-history');
        if(clicked == false){
            $.ajax({
                // url: 'https://sharinghappiness.org/api/v1/program/'+slug[3]+'/program-updates',
                url: 'https://devel.sharinghappiness.org/api/v1/program/menggapaitanahsuci/program-updates',
                method: 'get',
                success: function(res){
                    programHistory.html('');
                    if(res.status == 20) {
                        var data = res.result.data
                        clicked = true;
                        if(data.length > 0){
                            $.each(data,function(key,val){
                                var date = new Date();
                                var rep = val.created_at;
                                rep = rep.replace(/ /g,"T")
                                
                                // check browsers
                                if (ua.indexOf('safari') != -1) { 
                                    if (ua.indexOf('chrome') > -1) {
                                         // Chrome
                                        date = new Date(val.created_at)
                                    } else {
                                         // Safari
                                        date = new Date(rep);
                                    }
                                } else {
                                    date = new Date(val.created_at)
                                }
                                programHistory.append('\
                                    <li class="program-history-item">\
                                        <div class="program-history-text">\
                                            <small class="muted">'+ date.getDate() + " " + months[date.getMonth()] + " " + date.getFullYear()+'</small>\
                                            <span>'+val.title+'</span>\
                                            <span class="text-center text-highlight mb-4"></span>\
                                            <div class="content'+val.id+'"></div>\
                                        </div>\
                                        <div class="program-history-img">\
                                        </div>\
                                    </li>\
                                ')
        
                                // render code html in javascript
                                $('.content'+val.id).html(
                                    $('<div/>', {
                                        html: val.content
                                    }).text()
                                )
                            })
                            $('.noupdate-js').hide()
                        } else {
                            $('.noupdate-js').show()    
                        }
                    } else {
                        $('.noupdate-js').show()
                    }
                    $('.load-program-history').hide()
                }
            })
        }
    })

</script>
